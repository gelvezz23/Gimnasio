    public void loadData(){
        if(clienteAqui != null){
        jTextField1.setText(clienteAqui.getNombre());
        jTextField6.setText(clienteAqui.getCedula());
        jTextField4.setText(clienteAqui.getTelefono());
        jTextField3.setText(clienteAqui.getApellido());
        jTextField2.setText(clienteAqui.getDireccion());
        jFormattedTextField1.setValue(clienteAqui.getFechaIngreso());
        }else{
        jTextField1.setText("");
        jTextField6.setText("");
        jTextField4.setText("");
        jTextField3.setText("");
        jTextField2.setText("");
        jFormattedTextField1.setText("");
        }
    }
 public void saveData(){
    if(clienteAqui == null){
        clienteAqui = new ClienteVO();
    }
        clienteAqui.setCedula(jTextField6.getText());
        clienteAqui.setApellido(jTextField3.getText());
        clienteAqui.setDireccion(jTextField2.getText());
        clienteAqui.setNombre(jTextField1.getText());
        clienteAqui.setTelefono(jTextField4.getText());
        clienteAqui.setFechaIngreso((Date)jFormattedTextField1.getValue());
    }

    public ClienteVO getClienteAqui() {
        return clienteAqui;
    }

    public void setClienteAqui(ClienteVO clienteAqui) {
        this.clienteAqui = clienteAqui;
    }
    
